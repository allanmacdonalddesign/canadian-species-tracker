const defaultData = {
  species: [
    {
      name: 'Species Data Loading...',
      scientificName: 'Data not available',
      status: 'Unknown',
      description: 'Species data for this province is being loaded. Please check back later.',
      population: 'Unknown',
      threats: ['Data not available']
    }
  ],
  trends: {
    overallDecline: 0,
    speciesAtRisk: 0,
    recoveryPrograms: 0,
    protectedAreas: 0
  }
};

export const getSpeciesData = (provinceCode) => {
  const provinceData = SPECIES_DATA[provinceCode];
  
  if (!provinceData) {
    return defaultData;
  }
  
  // Get all species for this province
  const allSpecies = provinceData.species || [];
  
  // Randomly shuffle and select 5
  const shuffled = [...allSpecies].sort(() => Math.random() - 0.5);
  const randomFive = shuffled.slice(0, Math.min(5, shuffled.length));
  
  // Return with only 5 random species
  return {
    species: randomFive,
    trends: provinceData.trends
  };
};
